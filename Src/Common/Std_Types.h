/**********************************************************************************************************************
 *  GLOBAL CONSTANT MACROS
 *********************************************************************************************************************/
# define STD_HIGH     1u /* 5V or 3.3V */
# define STD_LOW      0u /* 0V */

#define STD_OFF       0u
#define STD_ON        1u

//**************ADDRESS DEREFRENCE**************************************        
#define STD_DEREF(X)            (*((volatile uint32_t *)(X)))

//**************SET & CLEAR REG COMMAND****************************************        
#define STD_SET(REG, VALUE)  (REG |= VALUE)
#define STD_CLR(REG, VALUE)  (REG &= (~VALUE))

//**************SET & CLEAR BIT COMMAND****************************************        
#define STD_PIN_SET(REG,PIN)  (REG|=(STD_ON<<PIN))
#define STD_PIN_CLR(REG,PIN)  (REG&=(~(STD_ON<<PIN)))
#define STD_PIN_FLIP(REG,PIN)  (REG^=(STD_ON<<PIN))
#define STD_PIN_READ(REG,PIN)  ( 1 & ((REG)>>PIN))


//**************Bitwise Pin manipulation OFFSET**************************************** 
#define STD_PIN_Offset(n)         ( 1U<< ( 2U+(n)) )  

